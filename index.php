    function getUrlVars() {
        var vars = {};
        var parts = window.location.href.replace(/[?&]+([^=&]+)=([^&]*)/gi, function(m,key,value) {
            vars[key] = value;
        });
        return vars;
    }

    function addSid(target) {
        // URL params pass for CTA buttons

        var locationUrl = window.location.href;
        var urlParams  = locationUrl.substring(locationUrl.indexOf('?')+1);

        var sid = getUrlVars()["sid"];

        if (urlParams != '' && typeof(sid) != "undefined") {

            for(t in target) {
                var currentHref = target[t].getAttribute('href');
                if (currentHref.indexOf('?') == -1){
                    var endingSlash = '';
                    if (currentHref.substr(currentHref.length - 1) != '/') { endingSlash = '/'; }
                    target[t].setAttribute('href', currentHref+endingSlash+'?sid='+sid);
                } else {
                    target[t].setAttribute('href', currentHref + '&sid=' + sid);
                }
            }
        }
        for(t1 in target) {
            currentHref = target[t1].getAttribute('href');
            if (currentHref.indexOf('HTTP_REFERER') != -1) {
                if (document.referrer && document.referrer != 'undefined') {
                    currentHref = currentHref.replace('HTTP_REFERER', document.referrer);
                } else {
                    currentHref = currentHref.replace('HTTP_REFERER', '');
                }
            }
            if (currentHref.indexOf('HTTP_HOST') != -1) {
                if (window.location.hostname && window.location.hostname != 'undefined') {
                    currentHref = currentHref.replace('HTTP_HOST', window.location.hostname);
                } else {
                    currentHref = currentHref.replace('HTTP_HOST', '');
                }
            }
            target[t1].setAttribute('href', currentHref);
        }
    };
